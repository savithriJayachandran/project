package Array;
import java.util.*;
public class ArraySort {

	public static void main(String[] args) {
		int a[]= new int[] {11,25,03,14,35,06,17,48,0,100};
System.out.println("The element before sorting: ");
	for(int i:a) {
		System.out.println(i+ " ");
	}
	Arrays.sort(a);
	System.out.println("The element after sorting: ");
	for(int j:a) {
		System.out.println(j+ " ");
	}
	}
	}


